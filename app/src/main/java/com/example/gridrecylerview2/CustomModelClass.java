package com.example.gridrecylerview2;

public class CustomModelClass {
    int item;
    String name;

    public CustomModelClass(int item, String name) {
        this.item = item;
        this.name = name;
    }

    public int getItem() {
        return item;
    }

    public void setItem(int item) {
        this.item = item;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
